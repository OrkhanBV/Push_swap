#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*ptr_s;

	ptr_s = s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		ptr_s[i] = 0;
		i++;
	}
}
